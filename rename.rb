def traverse(path)
  begin
    if File.directory?(path)
      Dir.foreach(path) do |file|
        if file != "." and file != ".." 
          traverse(path + "/" + file) {|x| yield x}
        end
      end
    end
    yield path
  rescue
    puts "Error: #{$!}"
  end
end

root = ARGV[0] || Dir.pwd
traverse(root) do |file|
  next if file =~ /\.rb$/

  file.downcase
  new_file = file.clone
  mapping.each_key do |key|
    new_file.gsub!(key.encode('GBK', 'UTF-8'), mapping[key].encode('GBK', 'UTF-8'))
  end
  if file != new_file
    puts "  #{file} -> #{new_file}"
    File.rename(file, new_file)
  end
end
